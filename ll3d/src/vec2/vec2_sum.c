#include "ll3d.h"

t_vec2	*vec2_sum(t_vec2 const *v1, t_vec2 const *v2)
{
	t_vec2	*vr;

	if (!(vr = vec2_init()))
		return (NULL);
	vr->x = v1->x + v2->x;
	vr->y = v1->y + v2->y;
	return (vr);
}
